
public class Time {

	private int hour;
	private int minute;
	
	public Time(int hour, int minute) {
		this.hour = hour;
		this.minute = minute;
	}
	
	/**
	 * getter for hour
	 * @return hour
	 */
	public int getHour() {
		return this.hour;
	}
	
	/**
	 * getter for minute
	 * @return minute
	 */
	public int getMinute() {
		return this.minute;
	}
	
	
	
}
